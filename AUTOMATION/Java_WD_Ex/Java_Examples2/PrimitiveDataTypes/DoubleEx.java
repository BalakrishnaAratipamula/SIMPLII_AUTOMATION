package PrimitiveDataTypes;

public class DoubleEx {
	public static void main(String[] args) { //https://www.javatpoint.com/double-keyword-in-java
		Double dobj = new Double(23.45);
		double d = dobj.doubleValue();
		System.out.println(d);

		d = 7.83684987683688;
		System.out.println(d);

		d = 50;
		System.out.println(d);

		d = 56.34f;
		System.out.println(d);

		d = 34f;
		System.out.println(d);

	}
}
